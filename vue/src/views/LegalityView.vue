                    <li>Card-carrying patients over 18 years old can buy up to 3.5 grams of cannabis flower or 1 gram of cannabis concentrate per day from a licensed dispensary, but no more than 24 days in a month. Minors with permission from a custodial parent or legal guardian may obtain a card through a physician. However, residents with prior convictions may not qualify for the program.</li>

                    <h3>New Hampshire:</h3>
