                    <h3>Alaska:</h3>
                    <li>Adults over the age of 21 can legally use and possess up to an ounce of marijuana recreationally, though it is still illegal to consume in public and punishable by a fine of up to $100. Adults may also cultivate up to six marijuana plants in a household, but only three of the plants can be mature.</li>

                    <h3>Arizona:</h3>
                    <li>Adults 21 and over can use, possess and buy up to 1 ounce of marijuana of which not more than 5 grams of that allowable ounce can be marijuana concentrates. Medical card holders can posses more than twice that amount. However, smoking or vaping marijuana in public remains illegal. Adults 21 and older can legally cultivate up to six plants in a home.</li>

                    <h3>California:</h3>
                    <li>Adults over the age of 21 can legally buy and posses 1 ounce of cannabis or up to 8 grams of concentrated cannabis for recreational use. For medicinal use with a physician's prescription, the age drops to 18 and users have higher possession limits. The sale of cannabis is legal at licensed dispensaries and adults can grow up to six cannabis plants at home, though rules on cultivating requirements vary by cities and counties. Marijuana use in public spaces remains illegal.</li>

                    <h3>Colorado:</h3>
                    <li>Residents over the age of 21 can buy up to 1 ounce of marijuana, 8 grams of marijuana concentrate or marijuana products containing up to 800 milligrams of THC in a single transaction. The law limits possession to 2 ounces of "retail" marijuana, purchased from a licensed dispensary.</li>

                    <h3>Connecticut:</h3>
                    <li>Adults over the age of 21 are allowed to possess or consume up to 1.5 ounces (42.5 grams) of “cannabis plant material” and up to 5 ounces (141.7 grams) in a locked container in a home or in the trunk or locked glove box in the person’s vehicle.</li>

                    <h3>Delaware:</h3>
                    <li>People 21 and older can possess up to up to 1 ounce (28 grams) of leaf marijuana, 12 grams of concentrated marijuana, or marijuana products containing up to 750 milligrams of the psychoactive compound THC. Possession of more than an ounce of marijuana and public consumption would remain misdemeanors.</li>

                    <h3>Illinois:</h3>
                    <li>Residents over the age of 21 may purchase and possess up to 1 ounce of marijuana at any time. Non-residents may have 15 grams. However, consuming it in public places remains illegal.</li>

                    <h3>Maine:</h3>
                    <li>In Maine, anyone over 21 can possess up to 2.5 ounces (70.9 grams) of marijuana. An adult can also cultivate up to three flowering marijuana plants and 12 immature plants.</li>

                    <h3>Maryland:</h3>
                    <li>In Maryland, people 21 and older can possess up to 1.5 ounces of cannabis flower, 12 grams of concentrated cannabis or a total amount of cannabis products that does not exceed 750 mg THC.</li>

                    <h3>Massachusetts:</h3>
                    <li>Adults over the age of 21 possession of up to 1 ounce of marijuana outside the home and up to 10 ounces inside their homes. If you're caught with more than 2 ounces of marijuana outside the home, it's a criminal offense. Residents may also grow up to six plants per person and up to 12 plants per household, but not in public view. However, landlords may prohibit tenets from growing pot plants.</li>

                    <h3>Michigan:</h3>
                    <li>Michigan residents who are 21 or older can possess or transport up to 2.5 ounces (70.8 grams) of marijuana. They can grow up to 12 plants, although not in public view. They also can give 2.5 ounces (70.8 grams) to another person, but not for payment.</li>

                    <h3>Minnesota:</h3>
                    <li>Adults 21 and older can possess and travel in the state with 2 ounces of cannabis flower, 8 grams of concentrate and 800 milligrams worth of THC-containing edible products such as gummies and seltzers. They can have up to 2 pounds of cannabis flower at home.</li>

                    <h3>Missouri:</h3>
                    <li>Adults 21 and older can purchase and posses 3 ounces of marijuana. Medical patients are allowed to buy 6 ounces per 30-day period. Residents may also have up to six flowering plants at home, but are required to register for a cultivation card through the state's health department. The cost of the card is $100.</li>

                    <h3>Montana:</h3>
                    <li>Adults 21 and older can buy and possess an ounce of cannabis, up to 8 grams of concentrate or edibles containing up to 800 mg of THC — the main psychoactive compound in cannabis. But adult-use marijuana businesses are not allowed in counties where the majority of voters rejected the recreational marijuana ballot measure that passed in 2020.</li>

                    <h3>Nevada:</h3>
                    <li>Anyone who is over 21 with a valid ID can buy up to an ounce of pot or one-eighth of an ounce of edibles or concentrates. People can only use the drug in a private home as it remains illegal to consume it in public, except for at licenses businesses with established on-site consumption lounges. Residents can grow six plants a person, and 12 plants per household.</li>

                    <h3>New Jersey:</h3>
                    <li>Dispensaries are allowed to sell up to the equivalent of 1 ounce of cannabis, which means an ounce of dried flower, or 5 grams of concentrate or 1,000 milligrams of edibles, like gummies to adults 21 and older. The state decriminalized possession of 6 ounces (170 grams) or less of marijuana or about three-fifths of an ounce (17 grams) of hashish.</li>

                    <h3>New Mexico:</h3>
                    <li>In April 2021, New Mexico Gov. Michelle Lujan Grisham signed a bill allowing adults over the age of 21 to have up to two ounces of cannabis with them to grow up to six cannabis plants per person, or 12 per household.</li>

                    <h3>New York:</h3>
                    <li>Anyone 21 and over can buy and possess up to 3 ounces of cannabis or 24 grams of concentrated forms of the drug, such as oils. Individual adults may also grow up to six cannabis plants — three mature and three immature — in their home for personal use and up to 12 plants for a household.</li>

                    <h3>Ohio:</h3>
                    <li>Adults 21 and over can buy and possess up to 2.5 ounces of cannabis and to grow up to six plants per individual or 12 plants per household at home.</li>

                    <h3>Oregon:</h3>
                    <li>Adults over the age of 21can possess up to eight ounces of marijuana in their home and up to one ounce away from home. For edible products, the limits are 16 ounces for a solid form and 72 ounces for drinkables. Residents can grow up to four cannabis plants in the house. Consuming marijuana in public remains illegal.</li>

                    <h3>Rhode Island:</h3>
                    <li>Anyone 21 and older can buy and possess up to 1 ounce of cannabis with no more than 10 ounces (283 grams) for personal use kept at a person’s home. Resident may also grow up to six plants, with no more than three mature, at home.</li>

                    <h3>Vermont:</h3>
                    <li>Adults over the age of 21 can buy and possess up to 1 ounce of marijuana, two mature and four immature plants.</li>

                    <h3>Virginia:</h3>
                    <li>Anyone over the age of 21 can possess up to an 1 ounce of cannabis and grow up to four plants for personal use. The plants cannot be in public view.</li>

                    <h3>Washington:</h3>
                    <li>Adults 21 and older can buy up to an ounce of dried marijuana, 16 ounces of pot-infused solids, 72 ounces of pot-infused liquids or 7 grams of concentrated marijuana, like hashish.</li>

                    <h3>Washington, D.C.:</h3>
                    <li>Adults over the age of 21 can possess up to 2 ounces of marijuana and can may gift up to 1 ounce to another person as long as "nothing else of value changes hand." That because while recreational marijuana has been legalized in D.C., it's still illegal to buy and sell the drug. Adults can legally grow up to six marijuana plants, but only three of them can be mature.</li>
