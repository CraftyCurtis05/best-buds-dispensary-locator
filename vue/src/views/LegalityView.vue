                    <li>Any patient over 18 years old with a medical marijuana card can posses up to 8 ounces of marijuana in their home, up to an ounce of concentrated marijuana, up to 72 ounces of edible marijuana and to three ounces of marijuana on their person.  They can also cultivate up to six plants.</li>

