                    <li>Medical marijuana is legal in the state for patients over 19 years old with one of 31 qualifying conditions. Anyone purchasing either flower or topical products are subject to monthly limits: no more than 2.5 ounces at a time and no more than 6,000 milligrams of THC in topicals or concentrates.</li>

