                    <h3>South Dakota:</h3>
                    <li>Patients over the age of 18 with a "debilitating medical condition" can apply for a state-issued medical marijuana card. The card fee is $75 and carriers can use it to buy up 3 ounces of cannabis flower every 14 days. They can also register to obtain permission to cultivate up to two cannabis flowering plants and two cannabis plants that are not flowering.</li>

