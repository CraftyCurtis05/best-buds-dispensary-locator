                    <h3>Florida:</h3>
                    <li>Residents who are 18 and older and have one of 12 qualifying medical conditions may a medical marijuana card from a licensed physicians. Physicians cannot issue a certification for more than three 70-day supply limits of marijuana or more than six 35-day supply limits of marijuana in a form for smoking, and a 35-day supply for smokable marijuana must be 2.5 ounces or less.</li>

